#include<iostream>
using namespace std;

int main()
{
    int num_1;
    char ch;
    bool myValue;
    float f_num;
    double num_2;
    string name;

    cout << "The Size Of Int : " << sizeof num_1 << endl;
    cout << "The Size Of Char : " << sizeof ch << endl;
    cout << "The Size Of Boolean : " << sizeof myValue << endl;
    cout << "The Size Of Float : " << sizeof f_num << endl;
    cout << "The Size Of Double : " << sizeof num_2 << endl;
    cout << "The Size Of String : " << sizeof name << endl;

    return 0;
}